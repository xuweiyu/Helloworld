package test;

public class Math {
    public static int add(int x, int y) {
        return x + y;
    }

    public static int minute(int x, int y) {
        return x - y;
    }

    public static int multiply(int x, int y) {
        return x * y;
    }

    public static float divide(int x, int y) {
        return x * 1.0f / y;
    }
}
